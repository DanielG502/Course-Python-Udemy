#esta es la recomendable
conjunto = {1, 2, 3, 4, 5}
print(conjunto)
#añadimos un valor al final
conjunto.add(20)
print(conjunto)
#removemos un conjunto que se le indique, en este caso el valor
conjunto.remove(1)
print(conjunto)
#elimina un dato aleatorio
conjunto.pop()
print(conjunto)

#añadimos elementos literables
conjunto.update([1, 2, 3])
print(conjunto)

#limpiar un conjunto
conjunto.clear()
print(conjunto)