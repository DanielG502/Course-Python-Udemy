letra = "o"

# lower() esta funcion es para determinar la variable y que la reconozca aunque sea la letra mayuscula o minuscula, este metodo es para que la variable la reconozca y la convierta en mayuscula o nimuscila 
if letra.lower() == "a":
    print("Esta Vocal es la letra A")
elif letra.lower() == "e":
    print("Esta Vocal es la letra E")
elif letra.lower() == "i":
    print("Esta Vocal es la letra I")
elif letra.lower() == "o":
    print("Esta Vocal es la letra O")
else:
    print("Esta Vocal es la letra U")

