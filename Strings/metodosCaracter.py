cadena = 'estoy utilizando los Metodos de python'
cadena2 = 'ESTOY UTILIZANDO LOS METODOS DE PYTHON'
cadena3 = 'eStoY uTiLiZaNdO lOs MeToDoS dE PyThOn'
#con este metodo, se puede imprimir una cadena con todos los caracteres 
#en minuscula
#el parametro se llama lower

print(cadena2.lower())

#con el parametro upper podemos llamar y imprimir una cadena en Mayuscula

print(cadena.upper())

#parametro Capitalize, muestra el primer caracter en mayuscula

print(cadena.capitalize())

#parametro title, funciona para imprimir cada caracter inicial de una frase en mayuscula

print(cadena.title())

#parametro swapcase, imprime las letras mayusculas en minusculas y las
#minusculas en mayusculas

print(cadena3.swapcase())